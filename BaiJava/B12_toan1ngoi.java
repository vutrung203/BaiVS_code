package BaiJava;

public class B12_toan1ngoi {
    public static void main(String[] args) 
    {
        int a = 5;
        boolean b = false;
        System.out.println("- => gia tri : "+(-a));
        System.out.println("+ => gia tri : "+(+a));
        System.out.println("! => gia tri : "+(!b));
        System.out.println("========================");
        System.out.println("a = "+a);
        System.out.println("++a = "+(++a));
        System.out.println("a++ = "+(a++));
        System.out.println("a = "+a);
        System.out.println("--a = "+(--a));
        System.out.println("a-- = "+(a--));
        System.out.println("a = "+a);
    }
}
